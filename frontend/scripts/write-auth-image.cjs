const fs = require("fs");
const authImagePath = "g:/github/Perlax/frontend/src/components/AuthenticatedImage.jsx";
const uploadUrlPath = "g:/github/Perlax/frontend/src/utils/uploadUrl.js";
const authImage = [
"import { useEffect, useState } from 'react';",
"import { Image } from '@mantine/core';",
"import { fetchAuthenticatedUploadBlob } from '../utils/uploadUrl';",
"",
"export default function AuthenticatedImage({",
"    publicPath,",
"    alt = '',",
"    fallbackSrc,",
"    ...imageProps",
"}) {",
"    const [blobUrl, setBlobUrl] = useState('');",
"    const [failed, setFailed] = useState(false);",
"",
"    useEffect(() => {",
"        if (!publicPath) {",
"            setBlobUrl('');",
"            setFailed(false);",
"            return undefined;",
"        }",
"",
"        let cancelled = false;",
"        let objectUrl = '';",
"",
"        setFailed(false);",
"        setBlobUrl('');",
"",
"        fetchAuthenticatedUploadBlob(publicPath)",
"            .then((url) => {",
"                if (cancelled) {",
"                    URL.revokeObjectURL(url);",
"                    return;",
"                }",
"                objectUrl = url;",
"                setBlobUrl(url);",
"            })",
"            .catch(() => {",
"                if (!cancelled) setFailed(true);",
"            });",
"",
"        return () => {",
"            cancelled = true;",
"            if (objectUrl) URL.revokeObjectURL(objectUrl);",
"        };",
"    }, [publicPath]);",
"",
"    return (",
"        <Image",
"            src={failed ? fallbackSrc : blobUrl || undefined}",
"            alt={alt}",
"            fallbackSrc={fallbackSrc}",
"            {...imageProps}",
"        />",
"    );",
"}"
].join("\n");
const uploadUrl = [
"import { getApiOrigin } from './api';",
"",
"function getStoredAuthToken() {",
"    try {",
"        const raw = localStorage.getItem('user');",
"        if (!raw) return null;",
"        const user = JSON.parse(raw);",
"        return user?.token ?? user?.Token ?? null;",
"    } catch {",
"        return null;",
"    }",
"}",
"",
"function encodeUploadPath(publicPath) {",
"    const trimmed = String(publicPath || '').trim();",
"    if (!trimmed || /^https?:\\/\\//i.test(trimmed)) return trimmed;",
"    const path = trimmed.startsWith('/') ? trimmed : `/${trimmed}`;",
"    return path.split('/').map((segment, index) => {",
"        if (index === 0 && segment === '') return '';",
"        try { return encodeURIComponent(decodeURIComponent(segment)); }",
"        catch { return encodeURIComponent(segment); }",
"    }).join('/');",
"}",
"",
"export function resolveUploadUrl(publicPath) {",
"    if (!publicPath || typeof publicPath !== 'string') return '';",
"    const trimmed = publicPath.trim();",
"    if (/^https?:\\/\\//i.test(trimmed)) return trimmed;",
"    const origin = getApiOrigin();",
"    const path = encodeUploadPath(trimmed);",
"    const token = getStoredAuthToken();",
"    if (!token) return `${origin}${path}`;",
"    const separator = path.includes('?') ? '&' : '?';",
"    return `${origin}${path}${separator}access_token=${encodeURIComponent(token)}`;",
"}",
"",
"export async function fetchAuthenticatedUploadBlob(publicPath) {",
"    if (!publicPath || typeof publicPath !== 'string') {",
"        throw new Error('Ruta de archivo invalida');",
"    }",
"    const trimmed = publicPath.trim();",
"    const token = getStoredAuthToken();",
"    const headers = token ? { Authorization: `Bearer ${token}` } : {};",
"    const requestUrl = /^https?:\\/\\//i.test(trimmed) ? trimmed : `${getApiOrigin()}${encodeUploadPath(trimmed)}`;",
"    const response = await fetch(requestUrl, { headers });",
"    if (!response.ok) throw new Error(`No se pudo cargar el archivo (${response.status})`);",
"    const blob = await response.blob();",
"    return URL.createObjectURL(blob);",
"}"
].join("\n");
fs.writeFileSync(authImagePath, authImage, "utf8");
fs.writeFileSync(uploadUrlPath, uploadUrl, "utf8");
for (const p of [authImagePath, uploadUrlPath]) {
  const buf = fs.readFileSync(p);
  console.log(p.split("/").pop(), buf.includes(0) ? "BAD" : "utf8 ok");
}